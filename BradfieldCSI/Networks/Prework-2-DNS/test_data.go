package main

/* All the functions refer to data for the same actual response */

func sampleDNSHeader() DNSHeader {
	return DNSHeader{ID: 0x0001, Flags: 0x8180, QCount: 0x0001, AnsCount: 0x0001, RRCount: 0x0000, AddnlRRCount: 0x0000}
}

func sampleDNSQuestion() DNSQuestion {
	return DNSQuestion{Name: []byte{0x07, 0x65, 0x78, 0x61, 0x6d, 0x70, 0x6c, 0x65, 0x03, 0x63, 0x6f, 0x6d, 0x00}, Type: 0x0001, Class: 0x0001, Namestr: "example.com"}
}

func sampleDNSAnswer() DNSAnswer {
	return DNSAnswer{Name: []byte{0xc0, 0x0c}, Type: 0x0001, Class: 0x0001, TTL: 0x00001faf, RDLength: 0x0004, RData: []byte{0x5d, 0xb8, 0xd8, 0x22}, Namestr: "example.com"}
}

func sampleDNSMessage() DNSMessage {
	return DNSMessage{
		Header:    sampleDNSHeader(),
		Questions: []DNSQuestion{sampleDNSQuestion()},
		Answers:   []DNSAnswer{sampleDNSAnswer()},
	}
}

func sampleRawDNSMessage() []byte {
	return []byte{0x00, 0x01, 0x81, 0x80, 0x00, 0x01, 0x00, 0x01, 0x00, 0x00, 0x00, 0x00, // header
		0x07, 0x65, 0x78, 0x61, 0x6d, 0x70, 0x6c, 0x65, 0x03, 0x63, 0x6f, 0x6d, 0x00, // QName
		0x00, 0x01, // QType
		0x00, 0x01, // QClass
		0xc0, 0x0c, // AnsName pointer byte 12
		0x00, 0x01, // AnsType A
		0x00, 0x01, // AnsClass IN
		0x00, 0x00, 0x1f, 0xaf, // TTL 8111 secs
		0x00, 0x04, // Data Length 4
		0x5d, 0xb8, 0xd8, 0x22, // Address 93.184.216.34
	}
}
